def send_question(chat_id):
    data = user_state[chat_id]
    prof = data["profession"]
    index = data["question_index"]

    question, options, correct = professions[prof]["questions"][index]
    data["correct_answer"] = correct

    markup = types.ReplyKeyboardMarkup(resize_keyboard=True)
    for opt in options:
        markup.add(opt)

    bot.send_message(chat_id, f"‚ùì {question}", reply_markup=markup)

@bot.message_handler(func=lambda m: m.chat.id in user_state and "correct_answer" in user_state[m.chat.id])
def handle_answer(message):
    chat_id = message.chat.id
    data = user_state[chat_id]

    if message.text == data["correct_answer"]:
        data["score"] += 1
        bot.send_message(chat_id, "‚úÖ–ü—Ä–∞–≤–∏–ª—å–Ω–æ! –ú–æ–ª–æ–¥–µ—Ü!")
